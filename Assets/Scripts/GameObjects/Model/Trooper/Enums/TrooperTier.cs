/// <summary>
/// Value, representing combat experience and sense of a Trooper
/// </summary>
public enum TrooperTier
{
    /// <summary>
    /// Recently recruited Trooper, still feeling really uncomfortable in combat
    /// </summary>
    Rookie = 0,
    /// <summary>
    /// Trooper, who sniffed some gunpowder, so several bullets, 
    /// hitting cover near his face don't really make him panic
    /// </summary>
    Hardned = 1,
    /// <summary>
    /// Experinced warrior, allready used to permanent threats of combat
    /// </summary>
    Veteran = 2,
    /// <summary>
    /// Cold-blooded war machine, who came to the Hell and back home 
    /// several times and does not seems to have any fears
    /// </summary>
    Elite = 3
}
