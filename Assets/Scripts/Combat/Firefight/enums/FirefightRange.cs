/// <summary>
/// Ranges types of the firefight
/// </summary>
public enum FirefightRange 
{
    /// <summary>
    /// Closest range (0-50 meters)
    /// </summary>
    Close = 0,
    /// <summary>
    /// Medium range (50-100 meters)
    /// </summary>
    Medium = 1,
    /// <summary>
    /// Long range (100-200 meters)
    /// </summary>
    Long = 2,
    /// <summary>
    /// Extream range (200 meters and further)
    /// </summary>
    Extreme = 3
}
